class Solution {  //
       
       public static void print(String s){
           for(int i = 0; i<s.length(); i++){
               System.out.print(s.charAt(i));
           }
       }

    public static void main(String[] args) {
        // Write your code here
       
        String s = "abcdefgh";
       print(s);
       
    }
}
